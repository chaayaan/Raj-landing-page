<script>
(function() {
  'use strict';

  /* ═══ CUSTOM CURSOR ═══ */
  var cursor = document.getElementById('rgCursor');
  var cursorDot = document.getElementById('rgCursorDot');
  var cursorX = 0, cursorY = 0;
  var dotX = 0, dotY = 0;

  if (cursor && cursorDot && window.innerWidth > 900) {
    document.addEventListener('mousemove', function(e) {
      cursorX = e.clientX;
      cursorY = e.clientY;
      cursor.classList.add('rg-cursor-visible');
      cursorDot.classList.add('rg-cursor-visible');
    });

    document.addEventListener('mouseleave', function() {
      cursor.classList.remove('rg-cursor-visible');
      cursorDot.classList.remove('rg-cursor-visible');
    });

    // Hover effects
    var hoverElements = 'a, button, .rg-prod-card, .rg-client-card, .rg-sc-home-card, .rg-why-card, .rg-gal-item, .rg-chip, input, textarea';
    document.querySelectorAll(hoverElements).forEach(function(el) {
      el.addEventListener('mouseenter', function() { cursor.classList.add('rg-cursor-hover'); });
      el.addEventListener('mouseleave', function() { cursor.classList.remove('rg-cursor-hover'); });
    });

    function animateCursor() {
      dotX += (cursorX - dotX) * 0.15;
      dotY += (cursorY - dotY) * 0.15;
      cursor.style.left = cursorX + 'px';
      cursor.style.top = cursorY + 'px';
      cursorDot.style.left = dotX + 'px';
      cursorDot.style.top = dotY + 'px';
      requestAnimationFrame(animateCursor);
    }
    animateCursor();
  }

  /* ═══ GOLD PARTICLES ═══ */
  var canvas = document.getElementById('rgParticles');
  if (canvas) {
    var ctx = canvas.getContext('2d');
    var particles = [];
    var particleCount = 30;

    function resizeCanvas() {
      canvas.width = window.innerWidth;
      canvas.height = window.innerHeight;
    }
    resizeCanvas();
    window.addEventListener('resize', resizeCanvas);

    function Particle() {
      this.x = Math.random() * canvas.width;
      this.y = Math.random() * canvas.height;
      this.size = Math.random() * 2 + 0.5;
      this.speedX = (Math.random() - 0.5) * 0.3;
      this.speedY = (Math.random() - 0.5) * 0.3;
      this.opacity = Math.random() * 0.5 + 0.1;
      this.fadeSpeed = Math.random() * 0.005 + 0.002;
      this.fadeDir = 1;
    }

    for (var i = 0; i < particleCount; i++) {
      particles.push(new Particle());
    }

    function drawParticles() {
      ctx.clearRect(0, 0, canvas.width, canvas.height);
      particles.forEach(function(p) {
        p.x += p.speedX;
        p.y += p.speedY;
        p.opacity += p.fadeSpeed * p.fadeDir;
        if (p.opacity >= 0.6) p.fadeDir = -1;
        if (p.opacity <= 0.05) {
          p.fadeDir = 1;
          p.x = Math.random() * canvas.width;
          p.y = Math.random() * canvas.height;
        }
        if (p.x < 0 || p.x > canvas.width) p.speedX *= -1;
        if (p.y < 0 || p.y > canvas.height) p.speedY *= -1;

        ctx.beginPath();
        ctx.arc(p.x, p.y, p.size, 0, Math.PI * 2);
        ctx.fillStyle = 'rgba(212, 168, 58, ' + p.opacity + ')';
        ctx.fill();
      });
      requestAnimationFrame(drawParticles);
    }
    drawParticles();
  }

  /* ═══ PARALLAX HERO ═══ */
  var heroImg = document.getElementById('rgHeroImg');
  if (heroImg) {
    window.addEventListener('scroll', function() {
      var scrollY = window.scrollY;
      if (scrollY < window.innerHeight) {
        heroImg.style.transform = 'scale(' + (1.1 + scrollY * 0.0002) + ') translateY(' + (scrollY * 0.3) + 'px)';
      }
    }, { passive: true });
  }

  /* ═══ SCROLL REVEAL ═══ */
  var reveals = document.querySelectorAll('.rg-reveal');
  var revealObserver = new IntersectionObserver(function(entries) {
    entries.forEach(function(entry) {
      if (entry.isIntersecting) {
        entry.target.classList.add('rg-visible');
        revealObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1, rootMargin: '0px 0px -50px 0px' });

  reveals.forEach(function(el) { revealObserver.observe(el); });

  /* ═══ COUNTER ANIMATION ═══ */
  var statItems = document.querySelectorAll('.rg-stat-item');
  var counterStarted = false;

  var counterObserver = new IntersectionObserver(function(entries) {
    entries.forEach(function(entry) {
      if (entry.isIntersecting && !counterStarted) {
        counterStarted = true;
        startCounters();
        counterObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.3 });

  statItems.forEach(function(item) { counterObserver.observe(item); });

  function startCounters() {
    statItems.forEach(function(item) {
      var target = parseInt(item.getAttribute('data-count'));
      var suffix = item.getAttribute('data-suffix') || '';
      var counter = item.querySelector('.rg-counter');
      var current = 0;
      var increment = target / 60;
      var duration = 2000;
      var startTime = null;

      function updateCounter(timestamp) {
        if (!startTime) startTime = timestamp;
        var progress = Math.min((timestamp - startTime) / duration, 1);
        var eased = 1 - Math.pow(1 - progress, 3); // ease out cubic
        current = Math.floor(eased * target);
        counter.textContent = current;
        
        if (progress < 1) {
          requestAnimationFrame(updateCounter);
        } else {
          counter.textContent = target;
          item.classList.add('rg-counted');
        }
      }
      requestAnimationFrame(updateCounter);
    });
  }

  /* ═══ SLIDER WITH PROGRESS ═══ */
  var slider = document.getElementById('rgSlider');
  var slides = slider ? slider.querySelectorAll('.rg-slide') : [];
  var dots = slider ? slider.querySelectorAll('.rg-sdot') : [];
  var progressBar = document.getElementById('rgSliderProgress');
  var prevBtn = document.getElementById('rgSliderPrev');
  var nextBtn = document.getElementById('rgSliderNext');
  var currentSlide = 0;
  var slideInterval = 6000;
  var slideTimer = null;
  var progressTimer = null;
  var progressStart = 0;

  function goToSlide(index) {
    slides[currentSlide].classList.remove('rg-active');
    dots[currentSlide].classList.remove('rg-active');
    currentSlide = (index + slides.length) % slides.length;
    slides[currentSlide].classList.add('rg-active');
    dots[currentSlide].classList.add('rg-active');
    resetProgress();
  }

  function resetProgress() {
    clearInterval(slideTimer);
    clearInterval(progressTimer);
    if (progressBar) progressBar.style.width = '0%';
    progressStart = Date.now();
    
    progressTimer = setInterval(function() {
      var elapsed = Date.now() - progressStart;
      var progress = Math.min((elapsed / slideInterval) * 100, 100);
      if (progressBar) progressBar.style.width = progress + '%';
    }, 30);

    slideTimer = setInterval(function() {
      goToSlide(currentSlide + 1);
    }, slideInterval);
  }

  if (prevBtn) prevBtn.addEventListener('click', function() { goToSlide(currentSlide - 1); });
  if (nextBtn) nextBtn.addEventListener('click', function() { goToSlide(currentSlide + 1); });
  dots.forEach(function(dot) {
    dot.addEventListener('click', function() {
      goToSlide(parseInt(this.getAttribute('data-i')));
    });
  });

  if (slides.length > 0) resetProgress();

  /* ═══ TESTIMONIAL CAROUSEL ═══ */
  var track = document.getElementById('rgTsTrack');
  var viewport = document.getElementById('rgTsViewport');
  var dotsWrap = document.getElementById('rgTsDots');
  var tsPrev = document.getElementById('rgTsPrev');
  var tsNext = document.getElementById('rgTsNext');

  if (track) {
    var cards = track.querySelectorAll('.rg-ts-card');
    var total = cards.length;
    var tsCurrent = 0;

    function visibleCount() {
      if (window.innerWidth <= 600) return 1;
      if (window.innerWidth <= 900) return 2;
      return 3;
    }

    function maxIdx() { return Math.max(0, total - visibleCount()); }

    function cardWidth() {
      if (!cards[0]) return 0;
      return cards[0].getBoundingClientRect().width + 24;
    }

    function goToTs(idx) {
      tsCurrent = Math.max(0, Math.min(idx, maxIdx()));
      track.style.transform = 'translateX(-' + (tsCurrent * cardWidth()) + 'px)';
      updateTsDots();
      if (tsPrev) tsPrev.disabled = tsCurrent === 0;
      if (tsNext) tsNext.disabled = tsCurrent >= maxIdx();
    }

    function buildTsDots() {
      if (!dotsWrap) return;
      dotsWrap.innerHTML = '';
      for (var i = 0; i <= maxIdx(); i++) {
        var btn = document.createElement('button');
        btn.className = 'rg-ts-dot' + (i === tsCurrent ? ' rg-ts-dot-active' : '');
        btn.setAttribute('data-idx', i);
        btn.addEventListener('click', (function(idx) { return function() { goToTs(idx); }; })(i));
        dotsWrap.appendChild(btn);
      }
    }

    function updateTsDots() {
      if (!dotsWrap) return;
      var d = dotsWrap.querySelectorAll('.rg-ts-dot');
      d.forEach(function(dot, i) {
        dot.classList.toggle('rg-ts-dot-active', i === tsCurrent);
      });
    }

    if (tsPrev) tsPrev.addEventListener('click', function() { goToTs(tsCurrent - 1); });
    if (tsNext) tsNext.addEventListener('click', function() { goToTs(tsCurrent + 1); });

    var resizeTimer;
    window.addEventListener('resize', function() {
      clearTimeout(resizeTimer);
      resizeTimer = setTimeout(function() { buildTsDots(); goToTs(Math.min(tsCurrent, maxIdx())); }, 120);
    });

    setInterval(function() { goToTs(tsCurrent >= maxIdx() ? 0 : tsCurrent + 1); }, 5000);

    buildTsDots();
    goToTs(0);
  }

  /* ═══ SMOOTH SCROLL FOR ANCHOR LINKS ═══ */
  document.querySelectorAll('a[href^="#"]').forEach(function(anchor) {
    anchor.addEventListener('click', function(e) {
      var href = this.getAttribute('href');
      if (href === '#') return;
      var target = document.querySelector(href);
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });

})();
