(function(){
  /* ── Canvas Particles ── */
  var c=document.getElementById('swHeroCanvas');
  if(!c)return;
  var ctx=c.getContext('2d'),particles=[],mouse={x:null,y:null};
  function resize(){c.width=window.innerWidth;c.height=window.innerHeight;}
  resize();
  window.addEventListener('resize',resize);
  for(var i=0;i<60;i++){
    particles.push({x:Math.random()*c.width,y:Math.random()*c.height,r:Math.random()*1.5+0.5,vx:(Math.random()-0.5)*0.3,vy:(Math.random()-0.5)*0.3,a:Math.random()*0.3+0.05});
  }
  c.addEventListener('mousemove',function(e){mouse.x=e.clientX;mouse.y=e.clientY;});
  c.addEventListener('mouseleave',function(){mouse.x=null;mouse.y=null;});
  function draw(){
    ctx.clearRect(0,0,c.width,c.height);
    for(var i=0;i<particles.length;i++){
      var p=particles[i];
      if(mouse.x!==null){var dx=mouse.x-p.x,dy=mouse.y-p.y,dist=Math.sqrt(dx*dx+dy*dy);if(dist<200){p.vx+=dx*0.00008;p.vy+=dy*0.00008;}}
      p.x+=p.vx;p.y+=p.vy;
      if(p.x<0)p.x=c.width;if(p.x>c.width)p.x=0;if(p.y<0)p.y=c.height;if(p.y>c.height)p.y=0;
      ctx.beginPath();ctx.arc(p.x,p.y,p.r,0,Math.PI*2);ctx.fillStyle='rgba(184,136,30,'+p.a+')';ctx.fill();
    }
    for(var i=0;i<particles.length;i++){
      for(var j=i+1;j<particles.length;j++){
        var dx=particles[i].x-particles[j].x,dy=particles[i].y-particles[j].y,dist=Math.sqrt(dx*dx+dy*dy);
        if(dist<120){ctx.beginPath();ctx.moveTo(particles[i].x,particles[i].y);ctx.lineTo(particles[j].x,particles[j].y);ctx.strokeStyle='rgba(184,136,30,'+(0.06*(1-dist/120))+')';ctx.lineWidth=0.5;ctx.stroke();}
      }
    }
    requestAnimationFrame(draw);
  }
  draw();

  /* ── Scroll Reveal ── */
  var els=document.querySelectorAll('.sw-reveal');
  var obs=new IntersectionObserver(function(entries){
    entries.forEach(function(e){if(e.isIntersecting){e.target.classList.add('rg-vis');obs.unobserve(e.target);}});
  },{threshold:0,rootMargin:'0px 0px -40px 0px'});
  setTimeout(function(){
    els.forEach(function(el){if(el.getBoundingClientRect().top>window.innerHeight){el.classList.add('rg-vis');}else{obs.observe(el);}});
  },60);

  /* ── Chart bar animation ── */
  var chartObs=new IntersectionObserver(function(entries){
    entries.forEach(function(e){if(e.isIntersecting){var bars=e.target.querySelectorAll('.sw-chart-bar');bars.forEach(function(b){b.style.height=parseFloat(b.style.getPropertyValue('--h'))+'%';});chartObs.unobserve(e.target);}});
  },{threshold:0.3});
  var chartEl=document.querySelector('.sw-chart');
  if(chartEl)chartObs.observe(chartEl);

  /* ── ERP Tab Switching ── */
  var tabs=document.querySelectorAll('.sw-erp-tab');
  var contents=document.querySelectorAll('.sw-erp-tab-content');
  tabs.forEach(function(tab){
    tab.addEventListener('click',function(){
      var target=tab.getAttribute('data-tab');
      tabs.forEach(function(t){t.classList.remove('sw-erp-tab-active');});
      tab.classList.add('sw-erp-tab-active');
      contents.forEach(function(c){c.classList.remove('sw-erp-tab-content-active');});
      var targetEl=document.getElementById('tab-'+target);
      if(targetEl)targetEl.classList.add('sw-erp-tab-content-active');
    });
  });

  /* ── Sync laptop & phone scroll positions ── */
  var laptopScroll=document.querySelector('.sw-laptop-scroll');
  var phoneScroll=document.querySelector('.sw-phone-mock-scroll');
  if(laptopScroll&&phoneScroll){
    var syncing=false;
    laptopScroll.addEventListener('scroll',function(){
      if(syncing)return;syncing=true;
      var ratio=laptopScroll.scrollTop/Math.max(1,laptopScroll.scrollHeight-laptopScroll.clientHeight);
      phoneScroll.scrollTop=ratio*(phoneScroll.scrollHeight-phoneScroll.clientHeight);
      requestAnimationFrame(function(){syncing=false;});
    });
    phoneScroll.addEventListener('scroll',function(){
      if(syncing)return;syncing=true;
      var ratio=phoneScroll.scrollTop/Math.max(1,phoneScroll.scrollHeight-phoneScroll.clientHeight);
      laptopScroll.scrollTop=ratio*(laptopScroll.scrollHeight-laptopScroll.clientHeight);
      requestAnimationFrame(function(){syncing=false;});
    });
  }
})();
